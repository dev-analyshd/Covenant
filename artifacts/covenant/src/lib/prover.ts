// ── Credential secret management ─────────────────────────────────────────────
// IndexedDB-backed encrypted storage for credential secrets.
// Uses SubtleCrypto AES-256-GCM with a user-derived encryption key.

const DB_NAME = "covenant_secrets";
const STORE_NAME = "credentials";
const DB_VERSION = 1;

async function openDB(): Promise<IDBDatabase> {
  return new Promise((resolve, reject) => {
    const req = indexedDB.open(DB_NAME, DB_VERSION);
    req.onupgradeneeded = (e) => {
      const db = (e.target as IDBOpenDBRequest).result;
      if (!db.objectStoreNames.contains(STORE_NAME)) {
        db.createObjectStore(STORE_NAME, { keyPath: "id" });
      }
    };
    req.onsuccess = () => resolve(req.result);
    req.onerror = () => reject(req.error);
  });
}

async function deriveKey(passphrase: string, salt: Uint8Array): Promise<CryptoKey> {
  const baseKey = await crypto.subtle.importKey(
    "raw",
    new TextEncoder().encode(passphrase),
    "PBKDF2",
    false,
    ["deriveKey"]
  );
  return crypto.subtle.deriveKey(
    { name: "PBKDF2", salt, iterations: 100_000, hash: "SHA-256" },
    baseKey,
    { name: "AES-GCM", length: 256 },
    false,
    ["encrypt", "decrypt"]
  );
}

export interface StoredCredential {
  id: string;
  nullifier: string;
  tier: number;
  expiresAt: number;
  encryptedSecret: ArrayBuffer;
  iv: Uint8Array;
  salt: Uint8Array;
  kycProvider: string;
  issuedAt: number;
  renewedCount: number;
}

export async function storeCredentialSecret(
  credentialId: string,
  secret: string, // 32-byte hex
  metadata: {
    nullifier: string;
    tier: number;
    expiresAt: number;
    kycProvider: string;
    passphrase?: string;
  }
): Promise<void> {
  const passphrase = metadata.passphrase ?? "covenant_default_v1";
  const salt = crypto.getRandomValues(new Uint8Array(16));
  const iv = crypto.getRandomValues(new Uint8Array(12));
  const key = await deriveKey(passphrase, salt);

  const secretBytes = new Uint8Array(
    secret.replace("0x", "").match(/.{2}/g)!.map(h => parseInt(h, 16))
  );
  const encryptedSecret = await crypto.subtle.encrypt({ name: "AES-GCM", iv }, key, secretBytes);

  const db = await openDB();
  const tx = db.transaction(STORE_NAME, "readwrite");
  const entry: StoredCredential = {
    id: credentialId,
    nullifier: metadata.nullifier,
    tier: metadata.tier,
    expiresAt: metadata.expiresAt,
    encryptedSecret,
    iv,
    salt,
    kycProvider: metadata.kycProvider,
    issuedAt: Math.floor(Date.now() / 1000),
    renewedCount: 0,
  };
  tx.objectStore(STORE_NAME).put(entry);
  await new Promise<void>((res, rej) => { tx.oncomplete = () => res(); tx.onerror = () => rej(tx.error); });
}

export async function retrieveCredentialSecret(
  credentialId: string,
  passphrase = "covenant_default_v1"
): Promise<{ secret: string; metadata: Omit<StoredCredential, "encryptedSecret" | "iv" | "salt"> } | null> {
  const db = await openDB();
  const tx = db.transaction(STORE_NAME, "readonly");
  const stored: StoredCredential | undefined = await new Promise((res, rej) => {
    const req = tx.objectStore(STORE_NAME).get(credentialId);
    req.onsuccess = () => res(req.result);
    req.onerror = () => rej(req.error);
  });
  if (!stored) return null;

  const key = await deriveKey(passphrase, stored.salt);
  const decrypted = await crypto.subtle.decrypt({ name: "AES-GCM", iv: stored.iv }, key, stored.encryptedSecret);
  const secretHex = Array.from(new Uint8Array(decrypted)).map(b => b.toString(16).padStart(2, "0")).join("");

  const { encryptedSecret: _, iv: __, salt: ___, ...meta } = stored;
  return { secret: "0x" + secretHex, metadata: meta };
}

export async function listStoredCredentials(): Promise<Omit<StoredCredential, "encryptedSecret" | "iv" | "salt">[]> {
  const db = await openDB();
  const tx = db.transaction(STORE_NAME, "readonly");
  const all: StoredCredential[] = await new Promise((res, rej) => {
    const req = tx.objectStore(STORE_NAME).getAll();
    req.onsuccess = () => res(req.result);
    req.onerror = () => rej(req.error);
  });
  return all.map(({ encryptedSecret: _, iv: __, salt: ___, ...meta }) => meta);
}

export async function deleteStoredCredential(credentialId: string): Promise<void> {
  const db = await openDB();
  const tx = db.transaction(STORE_NAME, "readwrite");
  tx.objectStore(STORE_NAME).delete(credentialId);
  await new Promise<void>((res, rej) => { tx.oncomplete = () => res(); tx.onerror = () => rej(tx.error); });
}

