// ============================================================================
// Covenant — Client-side Prover
// ============================================================================
// Orchestrates proof generation:
//   1. Generate credential_secret client-side (browser crypto)
//   2. Send witness to proving API (server-side bb prove simulation)
//   3. Receive 256-byte UltraHonk proof + public inputs
//   4. Optionally verify off-chain before submitting on-chain
//
// In production: the witness stays client-side, only the proof request is sent.
// The proving API never sees the raw KYC data — only derived hashes.
// ============================================================================

const API_BASE = import.meta.env.BASE_URL + "api";

export interface CredentialWitness {
  kycProvider: string;
  riskScore: number;
  sourceOfFunds: string;
  country: string;
  credentialSecret: string; // 32-byte hex — generated by SubtleCrypto
}

export interface ProofResult {
  proof: string;          // 256-byte hex
  publicInputs: string[]; // [nullifier, tier_bytes, address_commitment, view_key_hash]
  witness: {
    nullifier: string;
    tier: number;
    addressCommitment: string;
    viewKeyHash: string;
    kycLeaf: string;
    merkleRoot: string;
    expiryTimestamp: number;
    riskScore: number;
    kycProvider: string;
    sourceOfFunds: string;
    country: string;
  };
  metadata: {
    proofSystem: string;
    curve: string;
    circuitName: string;
    constraintCount: number;
    proofSizeBytes: number;
    barretenbergVersion: string;
    generatedAt: string;
  };
}

export interface SettlementProofResult {
  proof: string;
  publicInputs: string[];
  witness: {
    settlementHash: string;
    senderCommitment: string;
    recipientCommitment: string;
    amountCommitment: string;
    viewKeyHash: string;
    fromAsset: string;
    toAsset: string;
    amount: string | number;
    timestamp: number;
    tier: number;
  };
}

export interface VerifyResult {
  valid: boolean;
  checks: Record<string, boolean>;
  transcript: string;
  proofHash: string;
  verifiedAt: string;
}

// ── Generate credential proof ─────────────────────────────────────────────────
export async function proveCredential(witness: CredentialWitness): Promise<ProofResult> {
  const resp = await fetch(`${API_BASE}/prove/credential`, {
    method: "POST",
    headers: { "Content-Type": "application/json" },
    body: JSON.stringify(witness),
  });
  if (!resp.ok) {
    const err = await resp.json().catch(() => ({ error: resp.statusText }));
    throw new Error(`Prove API error: ${err.error || resp.statusText}`);
  }
  return resp.json();
}

